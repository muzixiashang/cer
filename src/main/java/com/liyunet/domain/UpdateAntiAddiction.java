package com.liyunet.domain;

/**记录修改的身份证
 */
public class UpdateAntiAddiction {

    private Integer id;

    private String antiAddictionId;
    //姓名
    private String userName;
    //身份证号
    private String identityCard;
    //正面照片
    private String frontUrl;
    //背面照片
    private String backUrl;
    //是否成功 0没提交过 1成功，2驳回，4审核中
    private Integer state;
    //修改时间
    private String updateTime;

    private String level;

    private String uId;


    public String getAntiAddictionId() {
        return antiAddictionId;
    }

    public void setAntiAddictionId(String antiAddictionId) {
        this.antiAddictionId = antiAddictionId;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getIdentityCard() {
        return identityCard;
    }

    public void setIdentityCard(String identityCard) {
        this.identityCard = identityCard;
    }

    public String getFrontUrl() {
        return frontUrl;
    }

    public void setFrontUrl(String frontUrl) {
        this.frontUrl = frontUrl;
    }

    public String getBackUrl() {
        return backUrl;
    }

    public void setBackUrl(String backUrl) {
        this.backUrl = backUrl;
    }

    public Integer getState() {
        return state;
    }

    public void setState(Integer state) {
        this.state = state;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getuId() {
        return uId;
    }

    public void setuId(String uId) {
        this.uId = uId;
    }
}
